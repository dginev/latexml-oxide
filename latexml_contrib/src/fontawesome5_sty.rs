use latexml_package::prelude::*;

/// Runtime equivalent of `DefMacro!("\\faXxx[]", "\\faIcon[#1]{kebab}")`
/// for the 1373 trivial FontAwesome 5 icon convenience macros below.
///
/// Each `DefMacro!` literal-pair arm expands at compile time into
/// `parse_prototype!` + `compile_expansion!` calls that *inline* the
/// resulting `Tokens` constants into the generated function body —
/// adding up to ~2.0 MiB of `.text` for 1373 nearly-identical calls
/// (audit DEP-15). Routing through a single runtime helper drops the
/// `load_definitions` function to ~100 KiB at the cost of one extra
/// `format!` + `parse_prototype` + `tokenize_internal` per icon
/// (~7 ms aggregate at engine bootstrap, never on the hot path).
///
/// Behaviour matches the macro arm: parameter list `[]` (one optional
/// bracketed arg), expansion body packed via `Expandable::new`'s
/// default pack_parameters path.
fn def_fa5_icon(suffix: &str, kebab: &str) -> Result<()> {
  let proto = format!("\\fa{suffix}[]");
  let (cs, params) = parse_prototype(&proto, true)?;
  let body = mouth::tokenize_internal(&format!("\\faIcon[#1]{{{kebab}}}"));
  def_macro(cs, params, ExpansionBody::Tokens(body), None)?;
  Ok(())
}

