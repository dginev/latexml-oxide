/// Runtime equivalent of `DefMacro!("\\faXxx", "\\faIconFromMacro{faXxx}")`
/// for the 719 trivial FontAwesome 4 icon convenience macros below.
/// See `def_fa5_icon` in `fontawesome5_sty.rs` for the FA5 sibling.
/// Audit DEP-15: collapses each call site from a ~1.5 KiB inlined
/// `Tokens` constant to a single function-call instruction + 1-2
/// `&str` pointers.
fn def_fa4_icon(suffix: &str) -> Result<()> {
  let proto = format!("\\fa{suffix}");
  let (cs, params) = parse_prototype(&proto, true)?;
  let body = mouth::tokenize_internal(&format!("\\faIconFromMacro{{fa{suffix}}}"));
  def_macro(cs, params, ExpansionBody::Tokens(body), None)?;
  Ok(())
}

